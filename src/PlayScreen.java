    private void btnSubmitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSubmitActionPerformed
        try {
            timer.stop();
            DBConnection db = new DBConnection();
            Competitions comps = new Competitions(db);
            boolean created = false;
            for (int i = 0; i < comps.getArraySize(); i++) {
                if (comps.getCompetition(i).getUser2().equalsIgnoreCase(".") && 
                        !(comps.getCompetition(i).getUser1().equalsIgnoreCase
        (username))) {
                    db.updateCompetition(comps.getCompetition(i).
                            getCompetitionID(), username, song, "tbd");
                    created = true;
                    break;                   
                }
            }
 
            if (!created) {
                 db.addCompetition(username, ".", song, ".", 0, 0, ".");  
            }
                    
        
        } catch (SQLException ex) {
            Logger.getLogger(PlayScreen.class.getName()).log(Level.SEVERE, 
                    null, ex);
        }
    }//GEN-LAST:event_btnSubmitActionPerformed

