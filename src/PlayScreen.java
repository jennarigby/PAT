    public PlayScreen(String username) {
       
        
            initComponents();
            this.username = username;
            this.setResizable(false);
            Profile profile = new Profile(username);
            try {
                if (profile.getLevel().equalsIgnoreCase("1")) {
                    jTabbedPane1.remove(pnlCreateLevel2);
                    jTabbedPane1.remove(pnlCreateLevel3);
                }
                else{
                    if (profile.getLevel().equalsIgnoreCase("2")) {
                        jTabbedPane1.remove(pnlCreateLevel3);
                    }
                }
            } catch (SQLException ex) {
                Logger.getLogger(PlayScreen.class.getName()).log(Level.SEVERE,
                        null, ex);
            }
            
            TimerSec = Integer.parseInt(lblSeconds.getText());
            TimerMin = Integer.parseInt(lblMinutes.getText());
            timer = new Timer(1000, new ActionListener() {
                @Override
                public void actionPerformed(ActionEvent e) {
                    
                    if (TimerSec == 0) {
                        TimerSec = 60;
                        TimerMin--;
                    }
                    if (TimerMin < 0) {
                        JOptionPane.showMessageDialog(rootPane, "Please submit "
                                + "and/or save your song" ,"", HEIGHT);
                        
                        TimerMin = 0;
                        TimerSec = 0;
                        timer.stop();
                    }else
                        TimerSec--;
                    lblSeconds.setText("" + TimerSec);
                    lblMinutes.setText("" + TimerMin);
                }
            });
            timer.start();
            
    }

