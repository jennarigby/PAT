    private void btnUpload1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpload1ActionPerformed

        try
        {
        AudioFormat audioFormat = new AudioFormat(AudioFormat.Encoding
                .PCM_SIGNED, 44100, 16, 2, 4, 44100, false);
        
        DataLine.Info dataInfo = new DataLine.Info(TargetDataLine.class,
                audioFormat);
            if (!AudioSystem.isLineSupported(dataInfo)) {
                System.out.println("Not supported");
            }
        
        TargetDataLine targetLine = (TargetDataLine)AudioSystem.getLine
        (dataInfo);
        targetLine.open();
        String name = JOptionPane.showInputDialog
                ("Name your recording");
        JOptionPane.showMessageDialog(null, "Click OK to start recording");
        targetLine.start();
        song +=  name + "#"+ (minute*60*1000 + second*1000 + millisecond);
        Thread audioRecorderThread = new Thread(){
            @Override public void run(){
                AudioInputStream recordingStream = new AudioInputStream
        (targetLine);
                
                File outputFile = new File(name + ".wav");
                try
                {
                    AudioSystem.write(recordingStream, AudioFileFormat.Type.
                            WAVE, outputFile);  
                    
                }
                catch (IOException ex){
                    System.out.println(ex);
                }
               
            }
        };
               
        audioRecorderThread.start();
        JOptionPane.showMessageDialog(null, "Recording has started. Click OK "
                + "to stop recording");
        targetLine.stop();
        targetLine.close();
        jLabel1.setText(name);
        DBConnection db = new DBConnection();
        db.addUpload(name, username);
           
        
        }
        catch(Exception e){
            System.out.println(e);
        }
    }//GEN-LAST:event_btnUpload1ActionPerformed

    private void btnRecordActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRecordActionPerformed
        song = "";
        if (clickRecord) {

        } else{
            state = true;
            Thread t = new Thread(){
                public void run(){
                    clickRecord = true;
                    for (;;) {
                        if (state) {
                            try{
                                sleep(1);

                                if (millisecond > 1000) {
                                    millisecond = 0;
                                    second++;
                                }
                                if (second > 60) {
                                    millisecond = 0;
                                    second = 0;
                                    minute++;
                                }
                                if (minute >= 5) {
                                    millisecond = 0;
                                    second = 0;
                                    minute = 0;
                                }
                                lblMilliStopwatch.setText("" + millisecond);
                                millisecond ++;
                                lblSecondsStopwatch.setText("" + second);
                                lblMinuteStopwatch.setText("" + minute);
                            }
                            catch(Exception e){
                                System.out.println("Error while trying to "
                                        + "record.");
                            }

                        }
                        else{
                            break;
                        }
                    }
                }
            };
            t.start();
        }
    }//GEN-LAST:event_btnRecordActionPerformed

