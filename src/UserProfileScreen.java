    private void btnPlayActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPlayActionPerformed
        if (txfCreationName.getText().isBlank()) {
            
        }else{
            try {
                DBConnection db = new DBConnection();
                Songs songs = new Songs(db); 
                Users users = new Users(db);               
                songs.populateSongArray();
                
                //Finds the song using the songName and username, reads it using
                //a scanner and plays each audio by calling the Create class,
                //pausing the thread to make sure each audio plays on time
                for (int i = 0; i < songs.getArraySize(); i++) {
                    if (songs.getSong(i).getUsername().equals(username) && 
                    songs.getSong(i).getSongName().equals(txfCreationName.
                            getText())) {
                        String song = songs.getSong(i).getSong();
                        Scanner scLine = new Scanner(song).useDelimiter("#");
                        while (scLine.hasNext()) {
                        String audioClip = scLine.next() + ".wav";
                        int milliseconds = scLine.nextInt();
                        System.out.println(audioClip + " " + milliseconds);
                        Create create = new Create();
                        Thread.sleep(milliseconds);
                        create.playAudio(audioClip);
                        }
                    }
                }
                        
                  
            
            } catch (SQLException ex) {
                System.out.println("Error connecting to database.");
            } catch (InterruptedException ex) {
                System.out.println("Error pausing thread.");
            }
        }
    }//GEN-LAST:event_btnPlayActionPerformed

