    public UserProfileScreen(String username) {
        this.username = username;
        initComponents();
        this.setResizable(false);

        try {
            txfUsername.setText(username);
            Profile profile = new Profile(username);
            txfRank.setText(profile.getRank() + "");
            txfLevel.setText(profile.getLevel());
            txfScore.setText("" + profile.getScore());

            //Displays user's friends in list
            DBConnection db = new DBConnection();
            Friends allFriends = new Friends(db);
            DefaultListModel listFriends = new DefaultListModel();
            allFriends.populateFriendArray();
            for (int i = 0; i < allFriends.getArraySize(); i++) {
                if (allFriends.getFriend(i).getUsername().equalsIgnoreCase
                 (username)) {
                    listFriends.addElement(allFriends.getFriend(i).getFriend());
                    lstFriends.setModel(listFriends);
                }
            }

            //Displays songs
            Songs allSongs = new Songs(db);
            DefaultListModel listSongs = new DefaultListModel();
            allSongs.populateSongArray();
            for (int i = 0; i < allSongs.getArraySize(); i++) {
                if (allSongs.getSong(i).getUsername().equalsIgnoreCase
                   (username)) {
                    listSongs.addElement(allSongs.getSong(i).getSongName());
                    lstCreations.setModel(listSongs);
                }
            }

            //Display uploads
            Uploads allUploads = new Uploads(db);
            DefaultListModel listUploads = new DefaultListModel();
            allUploads.populateUploadsArray();
            for (int i = 0; i < allUploads.getArraySize(); i++) {
                if (allUploads.getUpload(i).getUsername().equalsIgnoreCase
                    (username)) {
                    listUploads.addElement(allUploads.getUpload(i).getSoundName
                     ());
                    lstUploads.setModel(listUploads);

                }

            }

        } catch (SQLException ex) {
            System.out.println("Error connecting to database");
        }
    }


