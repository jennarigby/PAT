    private void btnLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLoginActionPerformed
        usernameLogin = txfUsernameLogin.getText();
        passwordLogin = pwfPasswordLogin.getText();
        
        UserLogin login = new UserLogin(usernameLogin, passwordLogin, "", "", 
                "");
        try {
                login.checkLogin();
            } catch (SQLException ex) {
                Logger.getLogger(LoginScreen.class.getName()).log(Level.SEVERE,
                        null, ex);
            }
 
        if (login.isUserValid()) {
          this.dispose();   
          HomeScreen home = new HomeScreen(usernameLogin);
          home.setVisible(true);
        }else{
            JOptionPane.showMessageDialog(rootPane, "Username or password is"
                    + " incorrect, please try again" ,"", HEIGHT);
        }
        
        
    }//GEN-LAST:event_btnLoginActionPerformed

