    private void btnSignUpActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSignUpActionPerformed
        String usernameSignUp = txfUsernameSignUp.getText();
        String emailSignUp = txfEmailSignUp.getText();
        String passwordSignUp = txfPasswordSignUp.getText();
        UserLogin  login = new UserLogin("", "", usernameSignUp, emailSignUp,
                passwordSignUp);     
        
        try {  
              if (!(txfUsernameSignUp.getText().isBlank() || txfEmailSignUp.getText()
                .isBlank() || txfPasswordSignUp.getText().isBlank())) {                 
                 login.insertNewUser();           
        } 
              else{
                JOptionPane.showMessageDialog(rootPane, "All text boxes have to"
                        + " be filled in." ,"", HEIGHT);  
              }
            } catch (SQLException ex) {
                System.out.println("Error when connecting to database.");
            }
           
        if (login.isUserValid()) { 
          this.dispose();
          HomeScreen home = new HomeScreen(usernameSignUp);
          home.setVisible(true);
        }
         else{
            if (!(txfUsernameSignUp.getText().isBlank() || txfEmailSignUp.getText()
                .isBlank() || txfPasswordSignUp.getText().isBlank())) {  
        JOptionPane.showMessageDialog(rootPane, "Username is taken. Please "
                + "enter a different one." ,"", HEIGHT);
            }
        }
    }//GEN-LAST:event_btnSignUpActionPerformed

