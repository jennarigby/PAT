    private void btnAddFriendActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddFriendActionPerformed
        try {
            DBConnection db = new DBConnection();
            boolean notFriends = true;
            Friends friends = new Friends(db);
            for (int i = 0; i < friends.getArraySize(); i++) {
                if (friends.getFriend(i).getUsername().equalsIgnoreCase
                (username) && friends.getFriend(i).getFriend().equalsIgnoreCase
                 (searchedUser)) {
                    JOptionPane.showMessageDialog(rootPane, "You are already "
                            + "friends with this user.", "", HEIGHT);
                    notFriends = false;
                    break;
                } else {
                    if (txfUsername.getText().isBlank()) {
                        JOptionPane.showMessageDialog(rootPane, "User does not "
                                + "exist.", "", HEIGHT);
                        break;
                    }
                }
            }
            if (notFriends) {
                db.addFriend(username, searchedUser);
            }

        } catch (SQLException ex) {
            System.out.println("Error connecting to database.");
        }
    }//GEN-LAST:event_btnAddFriendActionPerformed

