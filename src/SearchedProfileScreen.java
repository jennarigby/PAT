    public SearchedProfileScreen(String searchedUser, String username) {
        try {
            this.searchedUser = searchedUser;
            this.username = username;
            initComponents();
            this.setResizable(false);

            Users users = new Users(db);
            Boolean exists = false;
            for (int i = 0; i < users.getArraySize(); i++) {
                if (users.getUser(i).getUsername().equalsIgnoreCase
                 (searchedUser)) {
                    exists = true;
                    break;
                } else {
                    exists = false;
                }

            }
            if (exists) {
                txfUsername.setText(searchedUser);
                Profile profile = new Profile(searchedUser);
                txfRank.setText(profile.getRank() + "");
                txfLevel.setText(profile.getLevel());
                txfScore.setText("" + profile.getScore());

                //Display friends
                Friends allFriends = new Friends(db);
                DefaultListModel listFriends = new DefaultListModel();
                allFriends.populateFriendArray();
                for (int i = 0; i < allFriends.getArraySize(); i++) {
                    if (allFriends.getFriend(i).getUsername().equalsIgnoreCase
                     (searchedUser)) {
                        listFriends.addElement(allFriends.getFriend(i)
                                .getFriend());
                        lstUserFriends.setModel(listFriends);
                    }
                }

                //Display songs
                Songs allSongs = new Songs(db);
                DefaultListModel listSongs = new DefaultListModel();
                allSongs.populateSongArray();
                for (int i = 0; i < allSongs.getArraySize(); i++) {
                    if (allSongs.getSong(i).getUsername().equalsIgnoreCase
                       (searchedUser)) {
                        listSongs.addElement(allSongs.getSong(i).getSongName());
                        lstUserCreations.setModel(listSongs);
                    }
                }

            } else {
                JOptionPane.showMessageDialog(rootPane, "User does not "
                        + "exist", "", HEIGHT);

            }

        } catch (SQLException ex) {
            System.out.println("Error when connecting to database.");
        }
    }

