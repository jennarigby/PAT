    private void btnRecordActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRecordActionPerformed
        if (clickRecord) {

        } else{
            state = true;
            Thread t = new Thread(){
                public void run(){
                    clickRecord = true;
                    for (;;) {
                        if (state) {
                            try{
                                sleep(1);

                                if (millisecond > 1000) {
                                    millisecond = 0;
                                    second++;
                                }
                                if (second > 60) {
                                    millisecond = 0;
                                    second = 0;
                                    minute++;
                                }
                                if (minute >= 5) {
                                    millisecond = 0;
                                    second = 0;
                                    minute = 0;
                                }
                                lblMilliStopwatch.setText("" + millisecond);
                                millisecond ++;
                                lblSecondStopwatch.setText("" + second);
                                lblMinuteStopwatch.setText("" + minute);
                            }
                            catch(Exception e){

                            }

                        }
                        else{
                            break;
                        }
                    }
                }
            };
            t.start();
        }
    }//GEN-LAST:event_btnRecordActionPerformed

